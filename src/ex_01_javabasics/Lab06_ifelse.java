package ex_01_javabasics;

public class Lab06_ifelse {
    public static void main(String[] args) {
        int number = -6;

        if (number > 0) {
            System.out.println("Positive");
        } else if (number < 0) {
            System.out.println("Negative");
        } else {
            System.out.println("0");
        }
    }
    }
